    public void Listarclientes(){
        List<Cliente> ListaCl = cl.Listar();
        model = (DefaultTableModel) tablaComprador.getModel();
        Object[] obj = new Object[6];
        for(int i = 0;i<ListaCl.size();i++){
            obj[0] = ListaCl.get(i).getDni();
            obj[1] = ListaCl.get(i).getNombre();
            obj[2] = ListaCl.get(i).getTelefono();
            obj[3] = ListaCl.get(i).getDireccion();
            model.addRow(obj);
        }
        tablaComprador.setModel(model);
    }
    public void Listarprod(){
        List<Producto> ListaPr = baspro.ListarProd();
        model = (DefaultTableModel) tablaProducto.getModel();
        Object[] obje = new Object[6];
        for(int i = 0;i<ListaPr.size();i++){
            obje[0] = ListaPr.get(i).getCodigo();
            obje[1] = ListaPr.get(i).getNombre();
            obje[2] = ListaPr.get(i).getStock();
            obje[3] = ListaPr.get(i).getPrecio();
            model.addRow(obje);
        }
        tablaProducto.setModel(model);
    }
    public void Limpiar(){
        for(int i = 0; i < model.getRowCount();i++){
            model.removeRow(i);
            i--;
        }
    }
