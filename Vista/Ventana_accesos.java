    private void txtCantidadKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadKeyPressed
        // TODO add your handling code here:
         if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(!"".equals(txtCantidad.getText())){
                String cod = txtCodigo.getText();
                String descripcion = txtDescripcion.getText();
                int cant = Integer.parseInt(txtCantidad.getText());
                double precio = Double.parseDouble(txtPrecio.getText());
                double total = cant * precio;
                int stock = Integer.parseInt(txtStock.getText());
                if(stock >= cant ){
                    item = item + 1;
                    model = (DefaultTableModel)TablaVentasNuevas.getModel();
                    for(int i = 0; i<TablaVentasNuevas.getRowCount();i++){
                        if(TablaVentasNuevas.getValueAt(i,1).equals(txtDescripcion.getText())){
                            JOptionPane.showMessageDialog(null,"El producto ya esta registrado");
                            return;
                        }
                    }
                    ArrayList lista = new ArrayList();
                    lista.add(item);
                    lista.add(cod);
                    lista.add(descripcion);
                    lista.add(cant);
                    lista.add(precio);
                    lista.add(total);
                    Object[] obje = new Object[5];
                    obje[0] = lista.get(1);
                    obje[1] = lista.get(2);
                    obje[2] = lista.get(3);
                    obje[3] = lista.get(4);
                    obje[4] = lista.get(5);
                    model.addRow(obje);
                    TablaVentasNuevas.setModel(model);
                    Pagar();
                    LimpiarVenta();
                    txtCodigo.requestFocus();
                }else{
                    JOptionPane.showMessageDialog(null,"Stock no disponible.");
                    
                }
            }
        }
    }//GEN-LAST:event_txtCantidadKeyPressed

