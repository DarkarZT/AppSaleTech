    private void Btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn1ActionPerformed
        String busqueda = metodos.Buscarusuario(Correo_ingreso.getText(), Contra_ingreso.getText());

        if (Correo_ingreso.getText().equals("Admin") && Contra_ingreso.getText().equals("Admin")) {
            JOptionPane.showMessageDialog(this, "Bienvenido(a)");
            Ventana_accesos accesos = new Ventana_accesos();
            accesos.access.setText("Administrador");
            accesos.setVisible(true);
            accesos.jButton1.setEnabled(true);
            accesos.jButton7.setEnabled(true);
            this.dispose();
        } else if (busqueda.equals("Usuario registrado")) {
            String busqueda_name = metodos.buscarInicio(Correo_ingreso.getText());
            JOptionPane.showMessageDialog(this, "Bienvenido(a) " + busqueda_name);
            Ventana_accesos accesos = new Ventana_accesos();
            accesos.access.setText("Usuario " + busqueda_name);
            accesos.jButton1.setEnabled(false);
            accesos.jButton7.setEnabled(false);
            accesos.setVisible(true);
            
            
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Usuario y/o Contrase침a incorrectos");
        }


    }//GEN-LAST:event_Btn1ActionPerformed

